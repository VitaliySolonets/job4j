package ru.job4j.calculator;

/**
 * Calculator
 *
 * @author Vitaly Solonets (solonets.80@mail.ru)
 * @version 1.0
 * @since 05.06.2019
 */

public class Calculator {

    /**
     * Addition method.
     *
     * @param first, second.
     * @return first + second.
     */
    public double add(double first, double second) {
        return first + second;
    }
    /**
     * Subtraction method.
     *
     * @param first, second.
     * @return first - second.
     */
    public double subtruct(double first, double second) {
        return first - second;
    }
    /**
     * Division method.
     *
     * @param first, second.
     * @return first / second.
     */
    public double div(double first, double second) {
        return first / second;
    }
    /**
     * Multiplication method.
     *
     * @param first, second.
     * @return first * second.
     */
    public double multiple(double first, double second) {
        return first * second;
    }

}
